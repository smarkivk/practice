package operator;
public class RightShifOp {
    public static void main(String[] args) {
        int x = 6;
        x >>= 3;
        System.out.println(x);
        int x2 = 8;
        x2 >>= 3;
        System.out.println(x2);

        int y = 5;
        y <<= 3;
        System.out.println(y);
    }
}

